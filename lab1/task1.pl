$y = 3;

# 3*2 + 2.5 = 8.5
print ($x + 2*$y);
print("\n");

# 7 / 3 = 2.33333333333333
print (7 / $y);
print("\n");

# целочисленное деление 7 / 3 = 2
print int (7 / $y);
print("\n");

# остаток от деления 7 % 3 = 1
print (7 % $y);
print("\n");

# остаток от деления 7 % 3 = 1
print (7.5 % $y);
print("\n");

# остаток от деления 7 % 2 = 1
print (7.5 % $x);
